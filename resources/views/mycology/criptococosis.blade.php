                        <h2>CRIPTOCOCOSIS </h2>
                        <p>
                            El Género <em>Cryptococcus</em> está constituido por distintas especies
                            relacionadas entre sí, si bien la especie descrita en la mayoría de los
                        casos clínicos como agente etiológico de criptococosis humana y animal es    <em>C. neoformans</em>, aunque también se han documentado infecciones por
                        otras especies de criptococos como el <em>Cryptococcus albidus</em> y    <em>laurenttii</em> (Kamalam y cols., 1977; Riley y cols., 1992; Horowitz y
                        cols., 1993). Recientemente, estas especies y    <em>C. humicolus y C. curvatus</em> han sido aisladas en pacientes HIV
                            positivos (Sugita y cols., 2000; Ikeda y cols., 2000 y 2002).
                        </p>
                        <p>
                            <em>Cryptococcus neoformans</em>
                            ha recibido diferentes denominaciones a lo largo de la historia, no
                        obstante, la denominación aceptada por los taxónomos actuales es la de    <em>Cryptococcus neoformans</em> (Tabla 1).
                        </p>
