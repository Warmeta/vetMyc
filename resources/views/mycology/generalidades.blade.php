                        <p>
                            <strong>CULTIVOS</strong>
                        </p>
                        <p>
                            Los medios de cultivo suelen llevar sales, una fuente hidrocarbonada
                            (azúcares diversos o miel), una fuente de N inorgánico (sales de amonio o
                            nitratos, etc.), N orgánico (sustancias proteicas, peptonas o aminoácidos),
                            vitaminas y oligoelementos.
                        </p>
                        <p>
                            Se suele añadir antibióticos para inhibir el crecimiento bacteriano
                            (cloranfenicol 0,5% g/l, o gentamicina 0,05% g/L) pero siempre que no
                            actúen sobre la especie a aislar.
                        </p>
                        <p>
                            También se añaden inhibidores del crecimiento de hongos contaminantes,
                            cicloheximida o actidiona, a una concentración de 0,05% g/L.
                        </p>
                        <p>
                            En ocasiones también se añaden sustancias colorantes: rojo fenol, sales de
                            tetrazolio, etc.
                        </p>
                        <p>
                            El pH ha de ser ligeramente ácido para inhibir el crecimiento bacteriano,
                            en medios con antibióticos el pH utilizado suele ser neutro.
                        </p>
                        <p>
                            La temperatura de cultivo, normalmente entre 25-30ºC, pero algunos casos
                            necesitan 37ºC.
                        </p>
                        <p>
                            Los medios utilizados suelen ser sólidos salvo excepciones, así tenemos:
                        </p>
                        <p>
                            Sabouraud dextrosa agar: a veces con antibióticos y actidiona.
                        </p>
                        <p>
                            Dermatofitos agar test
                        </p>
                        <p>
                            Habitualmente se siembra en un tubo con actidiona y otro sin actidiona, si
                            se sospecha de levaduras se pueden utilizar medios con sales de tetrazolio.
                            Si se sospecha de actinomicetales, Sabouraud dextrosa sin antibióticos.
                        </p>
                        <p>
                            <strong> Medios de identificación dermatofitos:</strong>
                        </p>
                        <p>
                            1-Sabouraud esporulación o agar-harina de maíz: producción de esporas
                            asexuales.
                        </p>
                        <p>
                            2-Patata-glucosa agar: producción de pigmentos.
                        </p>
                        <p>
                            3-Granos de arroz-agua: selección de crecimiento.
                        </p>
                        <p>
                            4-Extracto de levadura más pelos: perforación del pelo.
                        </p>
                        <p>
                            5-Agar-dextrosa-urea: ureasa.
                        </p>
                        <p>
                            <strong> Identificación para levaduras:</strong>
                        </p>
                        <p>
                            1- Suero inactivado: filamentación o blastosis.
                        </p>
                        <p>
                            2-Arroz-agar tween-80 (RAT): clamidosporas y pseudomicelio.
                        </p>
                        <p>
                            3-Zimograma C: fermentación azúcares.
                        </p>
                        <p>
                            4-Auxonograma C: aprovechamiento de azúcares.
                        </p>
                        <p>
                            5-Api-aux y Pasteur-Yeast-System: identificación completa de levaduras.
                        </p>
