                        <h3>PAPEL EN LA VIDA</h3>
                        <p>
                            Los hongos juegan un papel importantísimo en nuestra vida. Puede decirse
                            que no pasa un día sin que nos veamos beneficiados o perjudicados, directa
                            o indirectamente por ellos. Los hongos microscópicos se desarrollan sobre
                            variados sustratos orgánicos. En los seres vivos pueden producir
                            enfermedades o pueden formar parte de la flora de un individuo (intestinal,
                            vaginal) siendo necesaria su existencia para un correcto funcionamiento del
                            organismo, en ciertas ocasiones estos mismos pueden cambiar su acción a
                            patógena debido a alteraciones en el equilibrio de la flora. Por lo que los
                            hongos pueden actuar en simbiosis o cómo parásitos.
                        </p>
                        <p>
                            Los hongos desempeñan un importante papel en los cambios lentos pero
                            constantes que tienen lugar en nuestro entorno. Son responsables de la
                            desintegración de la materia orgánica, son causantes de la mayoría de las
                            enfermedades de las plantas y de algunas enfermedades de animales y del
                            hombre. Intervienen en la fermentación del pan, del vino, cerveza, cacao,
                            quesos. Son empleados en la fabricación de ácidos orgánicos, drogas
                            (ergometrina y cortisona), vitaminas, responsables de la fabricación de
                            antibióticos (Penicilina y Griseofulvina).
                        </p>
                        <p>
                            Son organismos destructivos y beneficiosos para la agricultura,
                            primeramente, por causar grandes plagas que destruyen las cosechas y
                            beneficiosos en cuanto a que descomponen los restos vegetales debido a su
                            capacidad para utilizar la celulosa, de esta manera los nutrientes de las
                            plantas pasan al suelo de forma disponible para las plantas. Finalmente, no
                            se puede pasar por alto que suponen para el paladar ciertos hongos como el
                            champiñón y otras setas comestibles.
                        </p>
                        <h2>TÉCNICAS MICOLÓGICAS</h2>
                        <p>
                            <strong>Toma de muestras:</strong>
                        </p>
                        <p>
                            La recogida de la muestra ha de ser realizada en condiciones asépticas. En
                            pacientes tratados hay que dejar 4-5 días sin tratamiento. Si se han
                            aplicado pomadas o polvos lavar con suero fisiológico y repetir si el
                            resultado fuera negativo.
                        </p>
                        <p>
                            <strong>Las muestras pueden ser: </strong>
                        </p>
                        <p>
                        <ol start="1" type="1">
                            <li>
                                escamas: cogidas por raspado con el borde del bisturí o con el de un
                                portaobjetos.
                            </li>
                            <li>
                                pelos: arrancados con pinzas de los bordes de la lesión, principalmente
                                aquellos que se vean rotos o de aspecto blanquecino.
                            </li>
                            <li>
                                uñas: raspar la uña por dentro y también del surco periungueal.
                            </li>
                            <li>
                                orina y líquido cefalorraquídeo: sedimentación previa.
                            </li>
                            <li>
                                esputos: recogidos en tarro estéril añadiendo igual cantidad de
                                solución 0,5% de N-acetil-L-cisteína y centrifugar.
                            </li>
                            <li>
                                exudados, heces o sangre: recogidos en tarros estériles.
                            </li>
                            <li>
                                biopsias
                            </li>
                        </ol>
