                            <strong>PATOGENIA DE LAS DERMATOFITOSIS</strong>
                        </p>
                        <p>
                            <strong>PATOGENIA</strong>
                        </p>
                        <p>
                            <strong>Debemos saber que:</strong>

                            No son microorganismos invasivos

                            Son Queratinofílicos (estrato córneo, pelo, uñas)

                            Crecimiento hacia abajo (dirección descamación)

                            Elaboración y excreción de toxinas (irritantes)

                            Según Bofill y col. (1996) las condiciones más favorables para la
                            germinación, crecimiento y multiplicación de las esporas de dermatofitos,
                            tienen lugar en el folículo piloso entre las dos vainas de la raíz del
                            pelo.

                            Las esporas del hongo se protegen en las grietas de la piel y en los
                            folículos pilosos. Después de germinar, las hifas del hongo crecen por el
                            interior del pelo (endotrix).

                            Las esporas llegadas a las escoriaciones de la piel germinan y se
                            desarrollan en la superficie cutánea por debajo de la capa de
                            <a href="http://www.monografias.com/trabajos/celula/celula.shtml">
                                células
                            </a>
                            queratinizadas, desde la cual pueden alcanzar también los folículos
                            pilosos, introduciéndose en ellos.

                        Según trabajos de inoculación experimental realizados con    <em>T. verrucosum</em>, la patogenia de la enfermedad puede considerarse en
                            4 fases:

                        <ol start="1" type="1">
                            <li>
                                incubación - durante este período, por lo general, entre 7 y 17 días
                                posteriores a la inoculación, se produce una invasión rápida del
                                estrato córneo y la porción proximal y superficial del folículo piloso,
                                observándose hifas vegetativas largas diseminadas en estos espacios.
                            </li>
                            <li>
                                En los vasos sanguíneos de la dermis pueden apreciarse numerosas
                                células mononucleares, cuya aparición obedece a mecanismos de
                                respuestas ante la presencia del germen maduración - también denominada
                                como fase de diseminación, comienza a partir de los 14 - 17 días
                                posteriores a la inoculación. Durante esta etapa, el hongo invade
                                progresivamente la porción queratinizada exterior de la vaina de la
                                raíz del folículo piloso y se produce, además, la formación primaria de
                                artrosporas (ectotrix) a nivel del conducto piloso - sebáceo. A los 21
                                días aproximadamente la proliferación de artrosporas es evidente en el
                                lumen del folículo piloso y porción queratinizada más blanda de la
                                vaina de la raíz del lecho de la maduración del pelo. A los 28 días
                                penetra la cutícula, invade la corteza del crecimiento activo del pelo,
                                en la cual puede apreciarse la formación endotrix de artrosporas. Ya en
                                este período pueden observarse hifas en el conducto piloso - sebáceo.
                                Entre 28 - 35 días, las hifas pueden verse en la zona queratohialina de
                                los folículos en proceso de involución. La fragmentación del pelo en
                                las porciones superiores es significativa en este momento.
                            </li>
                            <li>
                                En los primeros, ocurre entre 28 - 49 días, mientras que en los
                                terneros se produce alrededor de una semana más tarde. Por
                                vasodilatación capilar de la dermis se produce un exudado seroso
                                acompañado de numerosos LPMN (leucocito polimorfonuclear) que se
                                infiltran en el estrado córneo de la epidermis.
                            </li>
                            <li>
                                Las masas de células PMN junto con el exudado, infiltrados en la
                                epidermis con
                                <a
                                    href="http://www.monografias.com/trabajos14/administ-procesos/administ-procesos.shtml#PROCE"
                                >
                                    procesos
                                </a>
                                de acantosis y paraqueratosis forman las costras típicas. El exudado
                                invade los folículos pilosos, formando microabscesos cuya ruptura se
                                produce en la dermis circundante. En el lecho capilar de la dermis
                                media con frecuencia se observa un infiltrado perivascular
                                linfocitario. Fragmentos de pelos rodeados por masas de artrosporas
                                yacen en la región hiperqueratinizada de la corteza.
                            </li>
                            <li>
                                clímax de
                                <a
                                    href="http://www.monografias.com/trabajos36/inflamacion/inflamacion.shtml"
                                >
                                    inflamación
                                </a>
                                - la respuesta inflamatoria resulta más aguda en los animales adultos
                                que en los jóvenes.
                            </li>
                            <li>
                                regresión - se caracteriza por el nacimiento y desarrollo de nuevos
                                pelos en el folículo que ha sanado. Este período comienza entre 49 - 63
                                días posteriores a la inoculación en animales de todas las edades, pero
                                por lo común es más temprano en animales adultos. Durante esta fase es
                                posible que aún puedan observarse los hongos en algunos cortes
                                histológicos. Sin embargo, los cultivos de raspados de piel, resultan
                                negativos. En los exudados se aprecian hifas en
                                <a
                                    href="http://www.monografias.com/trabajos12/elorigest/elorigest.shtml"
                                >
                                    estado
                                </a>
                                degenerativo. En las áreas de microabscesos pisifoliculares comienza el
                                proceso de cicatrización, infiltrándose de tejido fibroso granular. En
                                las zonas perivasculares de dermis puede observarse infiltración de
                                linfocitos eosinófilos.
                            </li>
                        </ol>
