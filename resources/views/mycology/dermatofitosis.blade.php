                            <u>LUCHA</u>
                        </p>
                        <p>
                            Tratamiento de afectadas y sanas con Griseofulvina (keotonazol o
                            itraconazol, la griseofulvina en España es difícil de conseguir)

                            - Limpiar y desinfectar alojamiento con formalina al 5-10%.

                            - Cuarentena.

                            Se ha experimentado la inmunización de un colectivo animal cuya afección
                            por esta patología en 1979 era de un 70%, tras un periodo
                            inmunoprofiláctico la afección en 1987 se redujo hasta un 0%. Resultando
                            ser un método de prevención efectivo. Existen vacunas avirulenta a base de
                            cepas liofilizadas de <em>Trichophyton verrucosum</em> atenuadas con
                            radiación ultravioleta. La inmunidad aparece un mes después de la
                            revacunación y la protección contra la Tricofitosis experimental y natural,
                            en animales vacunados, tiene una duración de varios años. Respecto a las
                        vacunas inactivadas contiene cepas inactivadas de    <em>Trichophyton verrucosum</em> y <em>Trichophyton mentagrophytes</em>; es
                            utilizada en la profilaxis de la Tricofitosis en el ganado, especialmente
                            en condiciones estables desde el punto de vista epizootiológico. No
                            contamina el medio ambiente con las esporas virulentas y es recomendable
                            para la eliminación de estos agentes del ambiente y de la población de
                            animales sensibles. Puede ser utilizada para el tratamiento contra la
                            Tricofitosis bovina
