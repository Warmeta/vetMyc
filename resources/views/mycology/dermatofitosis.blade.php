                            Lufenuron (Program, Novartis) Está catalogado como un inhibidor del
                            desarroillo de insectos el cual interfiere en la síntesis de quitina,
                            principal componente de la pared celular de estos hongos. Está disponible
                            como producto pulguicida y funciona inhibiendo la síntesis de quitina, que
                            es una glicoproteína estructural y parte importante del exoesqueleto de la
                            pulga. Las pulgas que se alimentan de gatos tratados con lufenuron lo
                            transferirán a sus huevos. Estos huevos serán incapaces de eclosionar o las
                            larvas de crecer porque su exoesqueleto presenta malformaciones. Los
                            organismos fúngicos también tienen una pared celular compuesta de quitina,
                            por lo que el lufenuron ha despertado interés en el tratamiento de estas
                            infecciones. Se publicó un estudio en el año 2000 cuyos resultados
                            mostraban que la administración oral de lufenuron (51-266 mg/Kg) se
                            asociaba con un rápido y efectivo tratamiento de la infección por M canis
                            en gatos.

