                </p>
                <p>
                    · Proteinasas y fosfolipasas: Las cuales corresponden a enzimas que
                    favorecen la diseminación por los tejidos del hospedador
                </p>
                <p>
                    · Tigmotropismo: que permite encontrar discontinuidades entre las células y
