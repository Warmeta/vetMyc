                        <p>
                            <strong>TRICOFITOSIS BOVINA</strong>
                        </p>
                        <p>
                            Micosis cutánea que afecta a bovino de todas las edades. Afecta a piel,
                            pelo y tegumentos queratinizados. Se caracteriza por provocar lesiones que
                            se presentan como placas circulares, de colores blancos grisáceos, secos y
                            bien delimitados; se localizan en la cabeza y cuello, y en ocasiones en
                            miembros posteriores y anteriores y región escrotal. Enfermedad de
                            presentación normalmente enzoótica. Otras especies que se pueden ver
                            afectadas: oveja, cerdo, caballo y hombre ya que constituye una
                            antropozoonosis.
                        </p>
