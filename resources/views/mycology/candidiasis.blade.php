                    capacidad del hongo para desarrollar un crecimiento levaduriforme y
                    filamentoso, el cual favorece la evasión de los mecanismos defensivos del
                    hospedador. También existen otros tipos de factores de virulencia, tales
                    como:
                </p>
                <p>
