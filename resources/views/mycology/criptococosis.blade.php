                        <p>
                            Otras especies de criptococos se han visto implicadas, aunque con baja
                            casuística en patología veterinaria.<em> Cryptococcus laurentii</em>, se ha
                            relacionado también como productor directo de enfermedad en aves, y más
                            concretamente de un estornino bronceado verde, que exhibía pérdida de
                            plumaje en la parte trasera y en el área del pico (Decostere y cols.,
                            2003).
                        </p>
                        <p>
                            <strong>LESIONES</strong>
                        </p>
                        <p>
                            <strong> </strong>
                        </p>
                        <p>
                            Algunos autores han relacionado las características de la respuesta tisular
                            con la patocronia de la infección criptococócica describiendo lesiones
                            "gelatinosas" o "no reactivas" y lesiones "granulomatosas". Actualmente se
                            piensa que la morfopatología de la lesión se relaciona con el estado de los
                            mecanismos de defensa, tanto celulares como humorales.

                            El tipo granulomatoso (criptococoma o granuloma criptocócico), está
                            caracterizado por la presencia de pocos organismos, cierta necrosis, y un
                            infiltrado inflamatorio intenso formado por linfocitos, células
                            mononucleares y células gigantes. En contraste, el tipo gelatinoso está
                            caracterizado por la visualización de múltiples criptococos, asociado con
                            poca o ninguna reacción inflamatoria (Radentz y Elewski, 1992).

                            En la forma granulomatosa, existe una respuesta inflamatoria en la que
                            participan células multinucleadas gigantes (con decenas de criptococos
                            fagocitados), macrófagos (con o sin fagocitosis de elementos micóticos),
                            linfocitos, células plasmáticas y leucocitos neutrófilos que típicamente
                            son poco numerosos, ya que salvo en los abscesos, las lesiones no suelen
                            ser supurativas.

                            En la forma gelatinosa, no existe respuesta inflamatoria acompañante y las
                            lesiones son susceptibles de alcanzar un tamaño capaz de provocar un efecto
                            de masa (Nielsen y Davis, 1988).

                            Las lesiones en el caso de la criptococosis pulmonar, pueden desarrollarse
                            en cualquier parte del pulmón, y con frecuencia son bilaterales.
                            Radiológicamente se traducen en lesiones pseudotumorales constituidas por
                            masas o nódulos pulmonares únicos o múltiples con tumefacción hiliar,
                            infiltrado parenquimatoso difuso, infiltrado peribronquial extenso y
                            lesiones miliares difusas. Rara vez hay formación de cavernas (Inui y
                            cols., 1998). También se han descrito neumonías segmentarias con derrame
                            pleural y una forma pulmonar fulminante (Santamaría y Zubero, 1995).

                            Las principales alteraciones anatomopatológicas en el SNC se localizan en
                            las meninges, sustancia gris cortical y núcleos basales. La respuesta
                            tisular es extremadamente variable. En pacientes inmunodeprimidos, los
                            microorganismos no inducen prácticamente reacción inflamatoria por lo que
                            las masas gelatinosas de hongos crecen en las meninges o en pequeños
                            quistes dentro de la sustancia gris como si fuera un medio de cultivo
                            (Cordero y cols., 1996; Jean y cols., 2002).

                            La presencia de criptococomas pulmonares y cerebrales es más característica
                            cuando la

                            afectación es por la variedad <em>gattii </em>(Speed y Dunt, 1995).

                            Las manifestaciones cutáneas se caracterizan por su polimorfismo y se han
                            descrito como nódulos subcutáneos (Manrique y cols., 1992), úlceras
                            (Schupbach y cols., 1976), celulitis (Gauder, 1987), púrpura palpable
                            (Greene, 1978), úlceras parecidas a pioderma gangrenoso (Massa y Doyle,
                            1981), lesiones herpetiformes (Borton y Wintroub, 1984), lesiones
                        semejantes a sarcoma de Kaposi (Blauvelt y Kerdel, 1992) y simulando a    <em>molluscum contagiosum</em><sup> </sup>(Concus y cols., 1988; Ricchi y
                            cols., 1991), que es la manifestación clínica más frecuente, de un tamaño
                            menor de 3 mm y ligeramente sobrelevadas, de localización en la extremidad
                            cefálica, cuello y extremidades. Pueden presentar costra central, de
                            aspecto varioliforme. Estas lesiones, en un contexto de fiebre y cuadro
                            meníngeo, deben hacer pensar en una criptococosis (Durden y Elewski, 1994).
                            En el caso de criptococosis cutánea primaria se observan lesiones de tipo
                            nodular, tumoral o pseudoverrugosa, con posterior abscedación y ulceración,
                            que suele localizarse en zonas descubiertas (brazos, piernas y cara)
                            (Bartralot-Soler y cols., 1995).

                        </p>
                        <p>
                            <strong>DIAGNÓSTICO</strong>
                        </p>
                        <p>
                            <strong> </strong>
                        </p>
                        <p>
                            Basándonos en los signos y síntomas clínicos, así como en la recogida de
                            datos epidemiológicos, podríamos presumir que nos encontramos ante un caso
                            de criptococosis, no obstante, debido a la inespecificidad de sus síntomas
                            (Pérez Cárdenas., 2003), ésta podría ser confundida con otras patologías,
                            principalmente con la producida por <em>Coccidioides immitis</em> ya que
                            ambas cursan con una meningoencefalitis de evolución crónica. También
                            podría confundirse con la tuberculosis ya que en ambas existe el desarrollo
                            de granulomas pulmonares (Cano Rangel y cols., 2002). Es por esta razón por
                            lo que para realizar un diagnóstico certero de esta enfermedad hemos de
                            recurrir a distintas pruebas laboratoriales en base al cultivo del hongo,
                            que es la técnica de referencia que permite razonablemente establecer el
                            diagnóstico (Figueroa, 1999; Matee y Matre, 2001).
                        </p>
                        <p>
                            <strong>FROTIS Y CULTIVO</strong>
                        </p>
                        <p>
                            A temperatura ambiente y a 37ºC en Agar Sabouraud glucosado, podemos
                            observar en un período de 36-72 horas el desarrollo de colonias mucoides al
                            principio blanquecinas, que se van tornando marronáceas con el paso del
                            tiempo. El tamaño de la cápsula y el tiempo de incubación determina la
                            cantidad de mucopolisacárido producido (García Rodríguez y Picazo, 1998).

                            <em>C. neoformans</em>
                            crece en el medio de Sabouraud, pero sin cicloheximida (actidiona), ya que
                            inhibe su crecimiento. Crece a 37 °C, pero lo hace más rápidamente a
                            temperaturas menores, como 30 °C<sup> </sup>(Diamond, 1997). También puede
                            crecer en cultivos de agar-sangre o agar-chocolate, pudiéndose obtener un
                            cultivo positivo en 24-72 h (Bartralot Soler y cols., 1995).

                            El tiempo de crecimiento en muestras de LCR varía desde 24-48 horas hasta
                            más de una semana, dependiendo de la densidad de microorganismos presentes
                            en la muestra.

                            El hemocultivo es una buena técnica de diagnóstico en estos pacientes; el
                            sistema de lisis-centrifugación es el más sensible y el más rápido en
                            detectar crecimiento pero también son sensibles y rápidos los sistemas de
                        hemocultivo con agitación y monitorización continua. El    <em>C. neoformans</em> también se puede cultivar del sedimento urinario,
                            especialmente tras masaje prostático, de muestras respiratorias y de
                            material de biopsia (Berenguer, 1996).
                        </p>
                        <p>
                            <strong>HALLAZGOS EN SANGRE Y LCR</strong>
                        </p>
                        <p>
                            En los pacientes sin SIDA, la concentración de glucosa en el LCR está
                            disminuída en la mitad de los casos, los niveles de proteína suelen ser
                            altos y es frecuente encontrar pleocitosis linfocitaria. No obstante, las
                            alteraciones del LCR son menos notorias en los pacientes con SIDA, y en
                            estadios de deterioro importante de la inmunidad, éste puede ser normal
                            (Diamond, 1995; Carrada Bravo, 2003).
                        </p>
                        <p>
                            El hongo puede también aislarse en sangre periférica, lo que ocurre hasta
                            en el 50% de los casos (Kisha y cols., 1994; Simpson y Berger, 1997).
                        </p>
                        <p>
                            Las levaduras encapsuladas pueden visualizarse en el LCR con preparaciones
                            de tinta china y resulta positivo con mayor frecuencia en los pacientes con
                            SIDA (Karstaedt y cols., 2002).
                        </p>
                        <p>
                            <strong>TINCIÓN NEGATIVA</strong>
                        </p>
                        <p>
                            <strong> </strong>
                        </p>
                        <p>
                            La cápsula del hongo puede ponerse de manifiesto al mezclar la suspensión
                            de levaduras con tinta china o nigrosina. Esta técnica es válida para el
                            examen directo del LCR, sedimento de orina, lavado broncoalveolar, exudados
                            de heridas e improntas de biopsias. La sensibilidad diagnóstica del examen
                            directo del LCR con tinta china es muy alta en pacientes infectados por el
                            VIH (Berenguer, 1996).
                        </p>
                        <p>
                            <strong>DIAGNÓSTICO HISTOPATOLÓGICO</strong>
                        </p>
                        <p>
                            Histopatológicamente, <em>C. neoformans</em> tiene un diámetro mayor de 7
                            µm, pero la cápsula mucoide que le rodea puede estar por encima de 30 µm.
                            Esta cápsula mucoide es una estructura característica en los tejidos. El
                            microorganismo casi siempre es incoloro y difícil de observar tinciones
                            rutinarias con hematoxilina y eosina (H-E). Las tinciones de metenamina
                            plata o del ácido periódico de Schiff (PAS) demarcan claramente el
                            microorganismo y permiten el reconocimiento de su tamaño y formas
                            características y su identificación como un microorganismo levaduriforme
                        que se reproduce por formación de brotes de base angosta.    <em>C. neoformans</em> también puede ser distinguido de otros hongos
                            levaduriformes y de los artefactos mediante la tinción de mucicarmín de
                            Mayer, que colorea la cápsula criptocócica de rojo o rosado, pero no tiñe
                            otros hongos que tienen características morfológicas similares. La cápsula
                            puede también teñirse con azul alcián. También puede utilizarse la tinción
                            argéntica de Masson-Fontana para melanina como tinción específica para el
                            diagnóstico histológico de criptococosis (Kwon-Chung y cols., 1981; Pérez y
                            Carrasco, 2000). Con la tinción de Gram se muestra una cápsula de color
                            rojo (Nelson y Couto, 2000).
                        </p>
                        <p>
                            <strong>
                                PRUEBAS DE DETECCIÓN DEL ANTÍGENO CAPSULAR. PRUEBA DE LÁTEX
                            </strong>
                        </p>
                        <p>
                            Entre las técnicas basadas en la detección de componentes fúngicos, está la
                            detección del antígeno capsular del <em>C. neoformans</em> por la técnica
                            de aglutinación de partículas de látex, que es útil en las muestras de
                            suero, LCR, orina e incluso en muestras respiratorias. Es una prueba que
                            tiene alta sensibilidad y especificidad y está comercializada, pero hay que
                            ser cautos en su interpretación. Se han descrito resultados falsos
                        positivos debidos a la presencia de factor reumatoide,    <em>Trychophyton beigelii</em>, <em>Capnocytophaga canimorsus</em>, y en el
                            suero de enfermos con septicemia o neoplasias. Las placas donde se realiza
                            la prueba deben estar libres de restos de desinfectantes y detergentes.
                            También se conocen falsos negativos, a veces por el fenómeno de prozona,
                            que se puede corregir diluyendo la muestra o tratándola con pronasa. La
                            sensibilidad es superior al 90%, en los pacientes con SIDA es incluso
                        mayor. Sin embargo, en este tipo de enfermos, se han descrito cepas de    <em>C. neoformans</em> con poca cápsula en los que la concentración de
                        antígeno puede ser anormalmente baja. La cuantificación del antígeno del    <em>C. neoformans</em> es útil para controlar la evolución de la
                            enfermedad, ya que el título desciende si la respuesta terapéutica es buena
                            y aumenta días antes de que se produzca una recaída, especialmente en el
                            LCR (Powderly y cols., 1994; Pontón, 2002; Cano Rangel y cols., 2002).
                        </p>
                        <p>
                            <strong>TOMOGRAFÍA Y RESONANCIA MAGNÉTICA</strong>
                        </p>
                        <p>
                            El conglomerado de los hongos y el material gelatinoso que produce
                            conforman lo que se conoce como pseudoquistes gelatinosos, pero que en
                            efecto corresponden a los criptococomas que se ven en la TAC (Tomografía
                            axial computerizada) y en la RM (resonancia magnética) en los ganglios de
                            la base, tálamo y mesencéfalo. Los criptococomas se ven en la RM como masas
                            redondas e hipointensas (Provenzale y Jinkins, 1997).
                        </p>
                        <p>
                            <strong>DIAGNÓSTICO MEDIANTE TÉCNICAS DE BIOLOGÍA MOLECULAR</strong>
                        </p>
                        <p>
                            Es una alternativa válida para el diagnóstico de las infecciones fúngicas,
                            especialmente para aquellas cuyo diagnóstico es difícil con las técnicas
                            convencionales. Estudios recientes muestran que la PCR (reacción en cadena
                            de la polimerasa) es una técnica sensible y específica en muestras
                            pulmonares y de LCR en medicina humana.
                        </p>
                        <p>
                            En veterinaria, en las últimas décadas la tecnología genético-molecular,
                            también ha experimentado un espectacular crecimiento. La RAPD–PCR (Random
                            Amplification of Polymorphic DNA - Polymerase Chain Reaction), consiste
                            básicamente en la amplificación al azar de ADN polimórfico mediante el uso
                            de cebadores de secuencia arbitraria y el uso de la PCR. Con esta técnica
                        se ha podido diferenciar los diferentes serotipos de <em>C.</em><em>neoformans</em> var. <em>neoformans</em> y <em>C. neoformans</em> var.    <em>gattii</em>, así como distintas especies de candidas, procedentes de
                            aislamientos de muestras clínicas y/o ambientales (Torres Rodríguez y
                            cols., 1999; Sansinforiano y cols., 2001).
                        </p>
                        <p>
                            <strong>TRATAMIENTO</strong>
                        </p>
                        <p>
                            El descubrimiento de la Anfotericina B (AFB), en 1956, proporcionó la
                            primera quimioterapia con buenos resultados (Pitisuttithum y cols., 2001),
                            no obstante, la mayor dificultad con el uso de AFB es su perfil de
                            toxicidad. La afectación renal es la reacción adversa más frecuente tras el
                            uso de este medicamento y se puede observar en el 80% de los casos que la
                            utilizan; además esta reacción se incrementa con la terapia concurrente con
                            otras drogas nefrotóxicas (Carlson y Condon, 1994).

                            Existen diversas combinaciones posibles en relación al tratamiento de esta
                            enfermedad, no obstante, actualmente, el tratamiento de elección es la
                            anfotericina B a dosis de 0.7-1 mg/kg/día durante 2 semanas o hasta
                            alcanzar una dosis acumulada de 1 ó 2 g seguido de fluconazol 400 mg al día
                            durante 6-8 semanas. El tratamiento combinado con 5-fluorocitosina no ha
                            demostrado beneficio clínico añadido. No se ha demostrado que la
                            combinación anfotericina B liposómica y anfotericina B complejo lipídico
                            sea de mayor eficacia pero sí su menor nefrotoxicidad con respecto a la
                            anfotericina B. Otra alternativa terapéutica es el tratamiento combinado de
                            fluconazol y 5-fluorocitosina, donde se han observado tasas de curación
                            elevadas (60-80%).<em> </em>El tratamiento de la criptococosis y la
                            quimioprofilaxis secundaria son a menudo imposibles en los países en
                            desarrollo o subdesarrollados debido al elevado costo y la escasa
                            disponibilidad de los fármacos requeridos, y a la necesidad de tratamiento
                            hospitalario y monitorización (Miller, 1999;<em> </em>Santamaría y Zubero,
                            2002).

                            Los pacientes infectados por <em>C. neoformans </em>var. <em>gattii </em>
                            requieren tratamientos más prolongados pues son más resistentes a la
                            terapia convencional. También necesitan más terapia intratecal,
                            procedimientos quirúrgicos y/o resecciones pulmonares (Speed y Dunt, 1995).
                            La mayor duración de la terapia para hospedadores no inmunosuprimidos con
                            infecciones por var. <em>gattii </em>parece estar relacionada con una alta
                            tasa de complicaciones, respuesta lenta y anormalidades clínicas
                            persistentes (Casadevall y Perfect, 1998).

                            La curación completa de la criptococosis en un paciente infectado por el
                            VIH es muy difícil, ya que a pesar de la curación clínica el criptococo
                            persiste en el LCR, plasma o próstata, por lo que el objetivo en estos
                            pacientes debe ser controlar la infección y mantener la profilaxis
                            secundaria de por vida para evitar recidivas (Dismukes, 1993), pues la
                            frecuencia de recurrencia llega al 70% (Iñigo García y cols., 1996). El
                            itraconazol puede ser una alternativa aunque se considera que el fluconazol
                            es más efectivo en la prevención de las recaídas.

                            Sin embargo, a partir de 1990 la frecuencia de esta enfermedad ha ido
                            disminuyendo en Europa y América tras el advenimiento de la terapia
                            antirretroviral de gran actividad (TARGA) y los tratamientos profilácticos
                            diseñados para prevenir las infecciones fúngicas, de tal forma que la
                            inmunosupresión originada por el virus de la inmunodeficiencia humana
                            (VIH), necesaria para que <em>Cryptococcus neoformans</em> infecte, se
                            retrasa indefinidamente (Kwon-Chung y cols., 2000; Manfredi, 2000; Quindós,
                            2002; Shelburne y Hamill, 2003). Al respecto, en nuestro país, la
                            incidencia actual de esta enfermedad tras la introducción de estos
                            tratamientos antirretrovirales ha disminuido desde 0.67 casos por millón de
                            habitantes en 1998 a 0.35 casos por millón de habitantes en 1999 (Colom y
                            cols., 2001).

                            En patología veterinaria, la respuesta al tratamiento de la criptococosis
                            nasal, respiratoria y cutánea es buena, especialmente si se detecta en los
                            estadíos iniciales de la enfermedad. Los gatos responden mal a la terapia
                            cuando manifiestan síntomas nerviosos. Se recomienda el uso de imidazoles
                            durante al menos 30 días. El itraconazol es el más efectivo y menos tóxico,
                            el fluconazol también ha dado buenos resultados, no obstante, el
                            inconveniente es el alto coste de ambos. Recientemente se ha ampliado el
                            uso de la flucitosina y el de la anfotericina B. Cuando se administra esta
                            última, debe valorarse la función renal dada su nefrotoxicidad y disminuir
                            la dosificación si se observan signos de insuficiencia renal. Se debe
                            administrar en infusiones lentas intravenosas respetando la dosis de
                            0.5-0.8 mg/Kg, de 2 a 3 veces por semana diluída en solución salina de
                            dextrosa, a razón de 400 ml para gatos y 500 ml para los perros. La
                            duración del tratamiento es de aproximadamente 4-6 semanas en el caso de
                            los gatos y de 6 a 8 para los perros. El ketoconazol es menos caro que el
                            itraconazol y el fluconazol, pero su principal problema es que no penetra
                            en el SNC. Dentro de los efectos adversos que se pueden presentar por el
                            uso del ketoconazol es la hepatotoxicidad que produce y la anorexia. En un
                            principio se puede usar una combinación de anfotericina B y flucitosina o
                            anfotericina B con ketoconazol para seguir posteriormente con la
                            administración de ketoconazol sólo. Para evaluar la eficacia del
                            tratamiento, se puede usar el test que detecta el antígeno polisacárido
                            capsular (Legendre y Berthelin, 1995; Hungerford y cols., 1998).
                        </p>
                        <p>
                            <strong>PROFILAXIS</strong>
                        </p>
                        <p>
                            Prevenir la exposición a <em>Cryptococcus neoformans</em> es una utopía. Ya
                            que no se puede evitar el contacto con el hongo, es aconsejable, sobre todo
                            en los pacientes en riesgo, evitar los lugares potencialmente contaminados
                            por el hongo, en particular aquellos con abundantes deyecciones de palomas,
                            aunque ninguna evidencia clara sustenta esta recomendación. En los enfermos
                            con un recuento de linfocitos CD4 &lt;100/ml, la administración
                            profiláctica de fluconazol disminuye la morbilidad de micosis sistémicas,
                            entre ellas la criptococosis (Powderly y cols., 1995). Sin embargo, la
                            aparición de resistencias, la escasa frecuencia de la criptococosis, los
                            efectos adversos de la medicación y su utilidad en el tratamiento de otras
                            micosis, hacen que no se recomiende rutinariamente la profilaxis primaria
                            de la criptococosis (Iñigo García y cols., 1996).
                        </p>
                        <p>
                            Es recomendable que estos pacientes eviten el contacto con este tipo de
                            ambientes y que nunca realicen personalmente la limpieza de palomares,
                            gallineros o desvanes, para evitar la inhalación de este hongo
                            (Blaschke-Hellmessen, 2000).
                        </p>
