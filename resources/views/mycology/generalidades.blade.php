                        <h3>ESTRUCTURA CELULAR Y REPRODUCCIÓN</h3>
                        <p>Los hongos son seres de organización eucariótica (célula diferenciada, con membrana nuclear y orgánulos citoplasmáticos), portadores de (conidios) esporas, desprovistos de clorofila, heterótrofos, unicelulares o filamentosos, con paredes celulares quitinosas o de otros carbohidratos complejos, disponen de enzimas que descomponen la materia orgánica en nutrientes sencillos y de fácil absorción. Son organismos que se reproducen de forma asexual (anamorfos) por esporas y a veces mediante reproducción sexual (telomorfos), en estos últimos dos hifas haploides se juntan para dar una hifa diploide, que posteriormente que posteriormente dan lugar a esporas que pueden estar encerradas en unas estructuras tipo asco o basidio, las esporas reciben el nombre de ascosporas o basiosporas.
                        El aparato vegetativo (talo) de los hongos está constituido por hifas cuyo conjunto forma un micelio vegetativo, este puede ser de dos tipos: unicelular (formado por células aisladas que se reproducen por gemación (Fig 1), como las levaduras), o filamentosos (con hifas septadas o tabicadas (Fig 3) o sin tabicar o cenocíticas (multinucleadas) (Fig 2). Existe un tipo intermedio, el pseudomicelio (Fig 4) formado por blastosporas como las que forman alguna Candida spp.
                        El conjunto de hifas se llama micelio. En el micelio se distinguen dos partes: una que penetra en el medio de cultivo y se extiende por su superficie (micelio vegetativo), y otra que se proyecta y contiene las esporas (micelio reproductor o aéreo).
                        Los denominados hongos dimórficos son aquellos que pueden presentar estructura como levadura o como filamentoso-micelial (moho) dependiendo de las condiciones ambientales en las que se desarrollen, la temperatura suele ser uno de estos factores. Ambas fases corresponden a un proceso de reproducción asexual del hongo.
                        </p> 
                        <p>
                        El micelio reproductor (micelio de fructificación), forma conidios (esporas) de origen sexual o asexual (conidios). Estas estructuras reproductoras se diferencian de las somáticas y son utilizadas para la clasificación de los hongos, pocos hongos pueden identificarse si estas estructuras no se encuentran disponibles, como acurre en el micelio esteril.
                        </br>
                        La espora (spora=semilla) es una unidad diminuta que se propaga sin un embrión y que da lugar a un nuevo individuo de la misma especie.
                        </br>
                        Los filamentos (hifas) que constituyen el soma fúngico crecen apicalmente.
                        </br>
                        La reproducción sexual es conocida como telomórfica: mediante zigosporas o blastosporas
                        </br>
                        La reproducción asexual es también denominada anamórfica, esta incluye la reproducción de las levaduras mediante gemación (de una célula madre sale un brote o yema que genera una nueva levadura) y también a la reproducción por esporas de los hongos pluricelulares o filamentosos. Existen hongos que sólo llevan a cabo la reproducción asexual, son los denominados hongos imperfectos o fungi imperfecti
                        </br>
                        </p>
